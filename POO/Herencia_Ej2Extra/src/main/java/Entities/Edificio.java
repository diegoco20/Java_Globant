package Entities;

/**
 *
 * @author diego
 */
public abstract class Edificio {
    protected int ancho;
    protected int alto;
    protected int largo;
    
    protected abstract void calcularSuperficie();
    protected abstract void calcularVolumen();

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }
    
    
}
